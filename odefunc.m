function y_ = odefunc(t, y)
    f = [306.25, 612.5, 918.75, 1225, 0, 0];
    m = [2.294e3, 1.941e3, 1.943e3, 2.732e3, 0.774e3, 0.774e3];
    k = [2155.059, 662.275, 1455, 778.436, 1 / (1/75.332 + 3 * deg2rad(8) / m(5)), 1 / (1/75.332 + 3 * deg2rad(8) / m(6))];
    c = 0.005 .* k;
    omega = 1.75;
    y_ = zeros(12, 1);
    y_(1) = y(2);
    y_(2) = (f(1) * sin(omega * t) - (c(1) + c(2)) * y(2) + c(2) * y(4) - (k(1) + k(2)) * y(1) + k(2) * y(3)) / m(1);
    y_(3) = y(4);
    y_(4) = (f(2) * sin(omega * t) + c(2) * y(2) - (c(2) + c(3)) * y(4) + c(3) * y(6) + k(2) * y(1) - (k(2) + k(3)) * y(3) + k(3) * y(5)) / m(2);
    y_(5) = y(6);
    y_(6) = (f(3) * sin(omega * t) +c(3) * y(4) - (c(3) + c(4)) * y(6) + c(4) * y(8) + k(3) * y(3) - (k(3) + k(4)) * y(5) + k(4) * y(7)) / m(3);
    y_(7) = y(8);
    y_(8) = (c(4) * y(6) - (c(4) + c(5) + c(6)) * y(8) + c(5) * y(10) + c(6) * y(12) + k(4) * y(5) - (k(4) + k(5) + k(6)) * y(7) + k(5) * y(9) + k(6) * y(11)) / m(4);
    y_(9) = y(10);
    y_(10) = (c(5) * y(8) - c(5) * y(10) + k(5) * y(7) - k(5) * y(9)) / m(5);
    y_(11) = y(12);
    y_(12) = (c(6) * y(8) - c(6) * y(12) + k(6) * y(7) - k(6) * y(11)) / m(6);
end
